int T, n;
VLL divs;

void read_input(){
    cin >> T;
}

void solve(){

    for(int t=0;t<T;t++){
        cin >> n;
        divs = VLL(n);
        for(int i=0;i<n;i++){
            cin >> divs[i];
        }
        sort(divs.begin(), divs.end());

        bool valid = true;
        LL num = divs[0] * divs[divs.size()-1];
        for(int l=0, r=divs.size()-1;l<=r;l++, r--){
            if(divs[l] * divs[r] != num){
                valid = false;
                break;
            }
        }

        if(valid){
            int l = 0;
            for(LL i=2;i*i<=num;i++){
                if(num % i == 0){
                    if(i != divs[l]){
                        valid = false;
                        break;
                    }else{
                        l++;
                    }
                }
            }
        }
        
        if(!valid){
            cout << "-1\n";
        }else{
            cout << num << "\n";
        }
    }
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    read_input();
    solve();
    return 0;
}
