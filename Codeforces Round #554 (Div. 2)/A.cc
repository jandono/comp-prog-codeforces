int n, m, chest, key, odd_chests=0, even_chests=0, odd_keys=0, even_keys=0;

void read_input(){
    cin >> n; // 1 <= n <= 100000
    cin >> m; // 1 <= m <= 100000

    for(int i=0;i<n;i++){
        cin >> chest;
        if(chest & 1) odd_chests++;
    }
    even_chests = n - odd_chests;

    for(int j=0;j<m;j++){
        cin >> key;
        if(key & 1) odd_keys++;
    }
    even_keys = m - odd_keys;
}

void solve(){

    cout << min(odd_chests, even_keys) + min(even_chests, odd_keys) << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    read_input();
    solve();
    return 0;
}
