int N, M;
VLL b, g;

void read_input(){
    cin >> N >> M; // 1 <= n, m <= 100 000

    b.resize(N);
    for(int i=0;i<N;i++){
        cin >> b[i];
    }

    g.resize(M);
    for(int i=0;i<M;i++){
        cin >> g[i];
    }
}

void solve(){

    sort(b.begin(), b.end(), greater<int>());
    sort(g.begin(), g.end());

    if(b[0] > g[0]){
       cout << "-1\n";
       return;
    }

    LL res = 0;
    for(int i=0;i<N;i++)
        res += b[i] * M;
    
    for(int i=1;i<M;i++)
        res += g[i] - b[0];

    if(g[0] != b[0])
        res += g[0] - b[1];

    cout << res << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    read_input();
    solve();
    return 0;
}
