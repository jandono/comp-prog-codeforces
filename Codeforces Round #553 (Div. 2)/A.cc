int operations(string s, int i){

    int res = 0;
    string genome = "ACTG";
    for(int j=0;j<4;j++){
        int c = s[i + j];

        if(c < genome[j]){
            res += min(genome[j] - c, c + 26 - genome[j]);
        }else{
            res += min(c - genome[j], genome[j] + 26 - c);
        }
    }

    return res;
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;

    string s;
    cin >> s;

    int result = INT_MAX;
    for(int i=0;i<=n-4;i++){
        result = min(operations(s, i), result);
    }
    cout << result << "\n";

    return 0;
}