int n;
VI a;

void read_input(){
    cin >> n;
    a.resize(2*n, 0);
}

void solve(){

    if(n % 2 == 0){
        cout << "NO\n";
        return;
    }

    int x = 1, flipped = 0;
    for(int i=0;i<n;i++){
        if(flipped){
            a[i] = x + 1;
            a[i+n] = x;
        }else{
            a[i] = x;
            a[i+n] = x + 1;
        }
        flipped ^= 1;
        x += 2;
    }

    cout << "YES\n";
    for(int i=0;i<2*n;i++) cout << a[i] << " ";
    cout << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    read_input();
    solve();
    return 0;
}
