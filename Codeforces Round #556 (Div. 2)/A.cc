int n, m, r;
int buy, sell;
int min_buy = INT_MAX;
int max_sell = INT_MIN;

void read_input(){
    cin >> n >> m >> r;
    for(int i=0;i<n;i++){
        cin >> buy;
        min_buy = min(min_buy, buy);
    }
    
    for(int i=0;i<m;i++){
        cin >> sell;
        max_sell = max(max_sell, sell);
    }
}

void solve(){
    
    int res = (r / min_buy) * max_sell + (r % min_buy);
    cout << max(r, res) << "\n";
}

int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    read_input();
    solve();
    return 0;
}
